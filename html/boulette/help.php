    <h1>Règles</h1>
    Tout d'abord chaque joueur choisi une catégorie suffisament vague. Si la catégorie est trop précise
    (exemple: "Présidents des États-Unis") le jeu risque d'être trop facile.
    Une fois toutes les catégories choisies, les joueurs doivent choisir un mot lié à chacune de celles-ci.
    C'est la dernière fois que les joueurs pourront voir les catégories alors il faut bien les mémoriser!

    <h2>Première ronde</h2>
    Le joueur "pige" un mot (sur le site, il faut cliquer sur les points rouges) et tente de le faire deviner
    aux autres par n'importe quelle méthode.

    <h2>Deuxième ronde</h2>
    Le joueur doit faire deviner le mot pigé en ne prononçant <i>qu'un seul mot</i>.

    <h3>Troisiême ronde</h2>
    Le joueur doit faire deviner le mot en mimant seulement!

